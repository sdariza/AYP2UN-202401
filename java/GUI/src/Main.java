    void addColumns(JTable table, String columns[]) {
        DefaultTableModel tableModel = new DefaultTableModel();
        for (int i = 0; i < columns.length; i++) {
            tableModel.addColumn(columns[i]);
        }
        table.setModel(tableModel);
    }

    void populateTable(JTable table, String data[][]) {
        String tempData[][] = new String[numUsers][columns.length];
        for (int i = 0; i < numUsers; i++) {
            for (int j = 0; j < columns.length; j++) {
                tempData[i][j] = data[i][j];
            }
        }
        DefaultTableModel dataModel = new DefaultTableModel(tempData, columns);
        table.setModel(dataModel);
    }

